#include <iostream>
using namespace std;

int main()
{
	int n1, n2;
	cout << "Enter n1: ";
	cin >> n1;

	cout << "Enter n2: ";
	cin >> n2;

	cout << "The sum of " << n1 << " and " << n2 << " is " << n1 + n2 << endl;
	cout << "The product of " << n1 << " and " << n2 << " is " << n1 * n2 << endl;
	cout << "The difference of " << n1 << " and " << n2 << " is " << n1 - n2 << endl;
}